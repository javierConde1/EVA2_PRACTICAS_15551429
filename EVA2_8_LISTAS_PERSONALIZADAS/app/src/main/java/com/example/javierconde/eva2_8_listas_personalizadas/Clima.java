package com.example.javierconde.eva2_8_listas_personalizadas;

public class Clima {
    String sCiudad;
    int iTemperatura;
    String sDescripcion;
    int iImagen;

    public Clima(String sCiudad, int iTemperatura, String sDescripcion, int iImagen){
        this.sCiudad = sCiudad;
        this.iTemperatura = iTemperatura;
        this.sDescripcion = sDescripcion;
        this.iImagen = iImagen;
    }
}
